let places : string[] = ["Makkah", "Russia", "Japan", "Kashmir", "Palestine"];
console.log("Original form");
console.log(places);

console.log("Alphabatic order");
console.log(places.sort());

places = ["Makkah", "Russia", "Japan", "Kashmir", "Palestine"];
console.log("Printing again to show that array is still in its Original form");
console.log(places);

console.log("Reverse Alphabetical order");
console.log(places.sort().reverse());

places = ["Makkah", "Russia", "Japan", "Kashmir", "Palestine"];
console.log("Showing again that array is still in originl form");
console.log(places);

console.log("Reversed order of the list");
console.log(places.reverse());

